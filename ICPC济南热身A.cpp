#include<stdio.h>
#include<iostream>
using namespace std;
int main()
{
	long long int i=1,N,t=0;
	cin>>N;
	for(;i<=N;i++)
	{
		if(i>9&&i<100)
		{
		    if(i/10==i%10)t++;
		}
		if(i>999&&i<10000)
		{
			if(i/100==i%100)t++;
		}
		if(i>99999&&i<1000000)
		{
			if(i/1000==i%1000)t++;
		}
		if(i>9999999&&i<100000000)
		{
			if(i/10000==i/10000)t++;
		}
		if(i>999999999&&i<10000000000)
		{
			if(i/100000==i%100000)t++;
		}
		if(i>99999999999&&i<1000000000000)
		{
			if(i/1000000==i%1000000)t++;
		}
		if(i>9999999999999&&i<100000000000000)
		{
			if(i/10000000==i%10000000)t++;
		}
		if(i>999999999999999&&10000000000000000)
		{
			if(i/100000000==i%100000000)t++;
		}
		if(i>99999999999999999&&1000000000000000000)
		{
			if(i/1000000000==i%1000000000)t++;
		}
	}
	cout<<t;
	return 0;
}
